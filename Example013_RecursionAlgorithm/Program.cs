// f(1) = 1;
// f(2) = 1;
// f(n) = f(n-1) + f(n-2)

double GetFibonacci(int num)
{
    if(num == 1 || num == 2) return 1;
    else return GetFibonacci(num - 1) + GetFibonacci(num - 2);
}

Console.WriteLine($"f({number}) = {GetFibonacci(number)}");
